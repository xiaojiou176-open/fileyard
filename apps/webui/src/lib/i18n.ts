export type MessageKey = keyof typeof messages.en
type TranslationValues = Record<string, string | number>

function normalizeLocale(raw?: string | null): Locale {
  const value = String(raw || '').trim().toLowerCase()
  if (value.startsWith('zh')) {
    return 'zh-CN'
  }
  return 'en'
}

function interpolate(template: string, values: TranslationValues = {}): string {
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(values[key] ?? `{${key}}`))
}

function resolveInitialLocale(): Locale {
  if (typeof window === 'undefined') {
    return 'en'
  }

  const searchLocale = new URLSearchParams(window.location.search).get('lang')
  if (searchLocale) {
    return normalizeLocale(searchLocale)
  }

  const storedLocale = window.localStorage.getItem(LOCALE_STORAGE_KEY)
  if (storedLocale) {
    return normalizeLocale(storedLocale)
  }

  if (typeof document !== 'undefined' && document.documentElement.lang) {
    return normalizeLocale(document.documentElement.lang)
  }

  return 'en'
}

export function translate(locale: Locale, key: MessageKey, values?: TranslationValues): string {
  return interpolate(messages[locale][key], values)
}

export function toIntlLocale(locale: Locale): string {
  return locale === 'zh-CN' ? 'zh-CN' : 'en-US'
}

interface I18nContextValue {
  locale: Locale
  setLocale: (locale: Locale) => void
  t: (key: MessageKey, values?: TranslationValues) => string
}

const defaultContext: I18nContextValue = {
  locale: 'en',
  setLocale: () => undefined,
  t: (key, values) => translate('en', key, values),
}

const I18nContext = createContext<I18nContextValue>(defaultContext)

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocale] = useState<Locale>(() => resolveInitialLocale())

  useEffect(() => {
    if (typeof document !== 'undefined') {
      document.documentElement.lang = locale
    }
    if (typeof window !== 'undefined') {
      window.localStorage.setItem(LOCALE_STORAGE_KEY, locale)
    }
  }, [locale])

  const value = useMemo<I18nContextValue>(
    () => ({
      locale,
      setLocale,
      t: (key, values) => translate(locale, key, values),
    }),
    [locale],
  )

  return createElement(I18nContext.Provider, { value }, children)
}

export const LocaleProvider = I18nProvider

export function useI18n() {
  return useContext(I18nContext)
}
